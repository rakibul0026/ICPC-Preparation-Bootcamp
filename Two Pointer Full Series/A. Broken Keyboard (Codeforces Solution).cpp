#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        string s;
        cin >> s;
        set<char> st;
        for (int i = 0; i < s.size(); i++)
        {
            if (s[i] == s[i + 1])
            {
                i++;
            }
            else
                st.insert(s[i]);
        }
        for (auto u : st)
        {
            cout << u;
            
        }
        cout << endl;
    }
}
